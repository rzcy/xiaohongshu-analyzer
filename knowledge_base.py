def get_category_knowledge(category: str) -> dict | None:
    """
    根据品类关键词返回对应的品类基准数据。

    参数：
        category: 品类名称（如 "美妆"、"穿搭"、"美食" 等）

    返回：
        包含品类基准数据的字典，若未匹配到则返回 None
    """
    if not category:
        return None

    # 先尝试直接匹配
    normalized = _CATEGORY_KEYWORDS.get(category)
    if not normalized:
        # 模糊匹配：检查 category 是否包含任何关键词
        for keyword, cat_name in _CATEGORY_KEYWORDS.items():
            if keyword in category:
                normalized = cat_name
                break

    if normalized and normalized in _CATEGORY_BENCHMARKS:
        result = _CATEGORY_BENCHMARKS[normalized].copy()
        result["品类"] = normalized
        return result

    return None
